package com.hua.entity;

public class User
{
	
	private String id;
	
	private String name;
	
	public String getId()
	{
			return id;
	}
	
	public final void setId(final String id)
	{
		this.id = id;
	}
	
	public final String getName()
	{
		return name;
	}
	
	public final void setName(final String name)
	{
		this.name = name;
	}
	
}


